        <ol>
            <li>
                <h4>Margarina (fonte de gordura Trans):</h4>
                <p>Muita atenção aos produtos industrializados confeccionados à base de margarina como nuggets, biscoitos recheados, sorvete cremoso, croissant. São alimentos ricos em gordura trans. A margarina não deve ser utilizada em pães ou preparações, podem ser usados creme vegetal ou queijos magros ou na versão light com redução de gordura.</p>
            </li>

            <li>
                <h4>Frituras:</h4>
                <p>Batata frita, pastel, salgadinhos. Mesmo utilizando óleo vegetal (canola ou soja) ou azeite, se um alimento for utilizado em elevadas temperaturas ele passa por reações químicas oferecendo perigo à saúde, se tornando um óleo saturado. O ideal é fazer preparações assadas, cozidas e ensopadas. Ainda contém grande quantidade de sódio. Alimentos fritos são ricos em ácidos graxos saturados.</p>
            </li>

            <li>
                <h4>Carnes gordas:</h4>
                <p>Miúdos, bacon, costela, vísceras (fígado, coração), pernil, pele do frango, embutidos (salsichão, salsicha, mortadela, salame), são ricos em gorduras saturadas. Ao se preparar carnes, deve-se remover a gordura aparente e a pele (aves), pois a gordura penetra no interior da carne durante o preparo. Entre os tipos de preparação, deve-se dar preferência ao grelhado, bem passado, pois a carne mal passada com gordura apresenta as maiores taxas de gordura saturada. Dar preferência aos assados, cozidos, ensopados e grelhados.</p>
            </li>

            <li>
                <h4>Queijos “amarelos”:</h4>
                <p>Prato, coalho, emental, brie, cheddar, mussarela, parmesão, minas padrão, entre outros. Estão presentes em sanduíches e em inúmeras preparações, são alimentos ricos em gorduras saturadas. A crença popular de que queijos de cor branca são adequados à saúde cardiovascular deve ser revista. Queijos minas padrão, requeijão e cream cheese têm alto teor de gordura saturada. Qualquer queijo cujo principal ingrediente seja o leite integral será fonte de gorduras saturadas. Podem ser substituídos por cottage, ricota, requeijão light, minas frescal light (cuidado com o tamanho da fatia), queijo prato light, mas sempre usando com moderação.</p>
